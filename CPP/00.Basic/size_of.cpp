// Using size of( different data types ):

#include <iostream>
using namespace std;

int main()
{
    cout << "Size of char             is " << sizeof(char)           << endl;
    cout << "Size of int8_t           is " << sizeof(int8_t)         << endl;
    cout << "Size of short            is " << sizeof(short)          << endl;
    cout << "Size of int16_t          is " << sizeof(int16_t)        << endl;
    cout << "Size of unsigned short   is " << sizeof(unsigned short) << endl;
    cout << "Size of int32_t          is " << sizeof(int32_t)        << endl;
    cout << "Size of int              is " << sizeof(int)            << endl;
    cout << "Size of float            is " << sizeof(float)          << endl;
    cout << "Size of long             is " << sizeof(long)           << endl;
    cout << "Size of unsigned         is " << sizeof(unsigned)       << endl;
    cout << "Size of double           is " << sizeof(double)         << endl;
    cout << "Size of long long        is " << sizeof(long long)      << endl;
    cout << "Size of int64_t          is " << sizeof(int64_t)        << endl;
    cout << "Size of string           is " << sizeof(string)         << endl;
    
    return 0;
}
